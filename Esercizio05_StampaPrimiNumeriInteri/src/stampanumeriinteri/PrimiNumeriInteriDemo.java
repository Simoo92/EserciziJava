package stampanumeriinteri;

public class PrimiNumeriInteriDemo {

	public static void main(String[] args) {

		final int NVOLTE = 10;
		int numeriInteri = 0;
		
		
		for (int i = 0; i < NVOLTE; i++) {
			numeriInteri = numeriInteri+1;
			System.out.println(numeriInteri);
			
		}
		
		
		
		
	}

}
